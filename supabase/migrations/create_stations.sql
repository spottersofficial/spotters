-- ============================================================
-- stations 테이블 생성
-- ============================================================
CREATE TABLE IF NOT EXISTS public.stations (
  id             SERIAL PRIMARY KEY,
  name           TEXT        NOT NULL UNIQUE,   -- 내부 키 (예: '홍대')
  display_name   TEXT        NOT NULL,           -- 표시명 (예: '홍대입구')
  lat            FLOAT       NOT NULL,
  lng            FLOAT       NOT NULL,
  radius         INT         NOT NULL DEFAULT 800,
  stop_words     TEXT        NOT NULL,           -- 정규식 문자열 (슬래시·플래그 제외)
  region_prefix  TEXT        NOT NULL,           -- 네이버 검색 키워드 접두사
  active         BOOLEAN     NOT NULL DEFAULT TRUE,
  last_updated   TIMESTAMPTZ          DEFAULT NOW()
);

